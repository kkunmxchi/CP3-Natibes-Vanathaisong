def addNumber1(x,y):
    print(x,"+",y,"=",x+y)
def addNumber2(x,y):
    print(x,"-",y,"=",x-y)
def addNumber3(x,y):
    print(x,"x",y,"=",x*y)
def addNumber4(x,y):
    print(x,"/",y,"=",int(x/y))

x = int(input("Input Number x = "))
y = int(input("Input Number y = "))
addNumber1(x,y)
addNumber2(x,y)
addNumber3(x,y)
addNumber4(x,y)